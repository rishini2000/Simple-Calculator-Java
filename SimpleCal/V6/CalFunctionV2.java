import javax.swing.JOptionPane;

public class CalFunctionV2{

    public static double getSum(double x, double y){
        double a = x+y;
        return a;
    }

    public static double getSub(double x, double y){
        double a = x-y;
        return a;
    }

    public static double getMul(double x, double y){
        double a = x*y;
        return a;
    }
    public static double getDiv(double x, double y){
        double a = x/y;
        return a;
    }

    public static double getRem(double x, double y){
        double a = x%y;
        return a;
    }
}
